#include <stdlib.h>
/**
 *add - function to execute
 *@a: ..
 *@b: ..
 *Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 *sub - function to execute
 *@a: ..
 *@b: ..
 *Return: sub
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 *mod - function to execute
 *@a: ..
 *@b: ..
 *Return: mod
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 *mul - function to execute
 *@a: ..
 *@b: ..
Return: mul
 */
int mul(int a, int b)
{
	return (a * b);
}

